/**
 * Build public/assets/taste/sprite.svg from the source taste SVGs.
 *
 * The sprite is the static placeholder shown before (and instead of) the Lottie:
 * it paints on first render with zero JS, so the tab row never flashes empty,
 * and it is what a reduced-motion visitor keeps looking at.
 *
 * Colours are remapped with the same table as bake-lottie.mjs so the static and
 * animated versions of an icon are the same drawing.
 *
 * Usage: node scripts/build-taste-sprite.mjs <srcDir>
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'

const srcDir = process.argv[2]
if (!srcDir) {
  console.error('usage: node scripts/build-taste-sprite.mjs <srcDir>')
  process.exit(1)
}

const ICONS = [
  { file: 'grapefruit (1).svg', id: 'bitter' },
  { file: 'lemon.svg', id: 'sour' },
  { file: 'strawberry (1).svg', id: 'sweet' },
  { file: 'chili-peppers.svg', id: 'spicy' },
  // zero deliberately has no icon — the tab stands on its label alone.
]

// Left as drawn. The static sprite and the Lottie are the same icon in two states,
// so any recolour has to apply to both or the icon visibly jumps when a tab is
// activated — keeping both at the pack's own colours is what holds them together.
const COLOR_MAP = {}

const symbols = ICONS.map(({ file, id }) => {
  let svg = readFileSync(join(srcDir, file), 'utf8')

  const viewBox = /viewBox="([^"]+)"/.exec(svg)?.[1] ?? '0 0 512 512'

  // Body = everything inside the root <svg>, minus the XML/comment preamble.
  svg = svg.slice(svg.indexOf('>', svg.indexOf('<svg')) + 1, svg.lastIndexOf('</svg>'))
  svg = svg.replace(/<!--[\s\S]*?-->/g, '')

  // Illustrator emits a tail of empty <g></g> pairs; drop them.
  svg = svg.replace(/<g>\s*<\/g>/g, '')

  // Fills default to black in SVG when unset, which is exactly the outline colour.
  svg = svg.replace(/#[0-9a-fA-F]{6}/g, (hex) => COLOR_MAP[hex.toLowerCase()] ?? hex)

  return `<symbol id="taste-${id}" viewBox="${viewBox}">${svg.trim()}</symbol>`
})

const body = symbols.join('')

// Emitted as a module, not just a file in public/: the sprite is inlined into the
// document so <use> can stay a same-document reference. External <use href="x.svg#id">
// is still broken in Safari, which is the browser most of these guests are on, and
// the venue page runs on edge where the file could not be read at request time anyway.
writeFileSync(
  'lib/taste-sprite.ts',
  '// GENERATED by scripts/build-taste-sprite.mjs — do not edit by hand.\n' +
    `export const TASTE_SPRITE = ${JSON.stringify(body)}\n\n` +
    '/** Tastes that actually have an icon. Anything else renders label-only. */\n' +
    `export const TASTE_ICONS: ReadonlySet<string> = new Set(${JSON.stringify(ICONS.map((i) => i.id))})\n`
)

console.log(
  `lib/taste-sprite.ts — ${ICONS.length} symbols, ${(body.length / 1024).toFixed(1)} KB raw`
)
