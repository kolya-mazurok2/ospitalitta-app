/**
 * Build lib/flag-sprite.ts — the country flags shown on the language control.
 *
 * Kept separate from the taste/UI sprite on purpose: flags are full-colour national
 * marks that must never be recoloured, while the chrome icons are driven by
 * currentColor. Two sprites keep those two rules from leaking into each other.
 *
 * Usage: node scripts/build-flag-sprite.mjs <srcDir>
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'

const srcDir = process.argv[2]
if (!srcDir) {
  console.error('usage: node scripts/build-flag-sprite.mjs <srcDir>')
  process.exit(1)
}

// Keyed by locale, not by country: the app asks "which flag for `sq`", and the
// answer (Albania) is not derivable from the locale code.
const FLAGS = [
  { locale: 'pl', file: 'poland.svg' },
  { locale: 'it', file: 'italy.svg' },
  { locale: 'fr', file: 'france.svg' },
  { locale: 'sq', file: 'albania.svg' },
  { locale: 'de', file: 'germany.svg' },
  { locale: 'no', file: 'norway.svg' },
  { locale: 'en', file: 'united-states.svg' },
  { locale: 'uk', file: 'ukraine.svg' },
  // `hu` has no flag in the drop — the control falls back to the globe for it.
]

const symbols = FLAGS.map(({ locale, file }) => {
  let svg = readFileSync(join(srcDir, file), 'utf8')

  const viewBox = /viewBox="([^"]+)"/.exec(svg)?.[1] ?? '0 0 512 512'

  svg = svg.slice(svg.indexOf('>', svg.indexOf('<svg')) + 1, svg.lastIndexOf('</svg>'))
  svg = svg.replace(/<!--[\s\S]*?-->/g, '')
  svg = svg.replace(/<g>\s*<\/g>/g, '')

  return `<symbol id="flag-${locale}" viewBox="${viewBox}">${svg.trim()}</symbol>`
})

const body = symbols.join('')

writeFileSync(
  'lib/flag-sprite.ts',
  '// GENERATED by scripts/build-flag-sprite.mjs — do not edit by hand.\n' +
    `export const FLAG_SPRITE = ${JSON.stringify(body)}\n\n` +
    '/** Locales that have a flag. Anything else falls back to the globe icon. */\n' +
    `export const FLAG_LOCALES: ReadonlySet<string> = new Set(${JSON.stringify(FLAGS.map((f) => f.locale))})\n`
)

console.log(`lib/flag-sprite.ts — ${FLAGS.length} flags, ${(body.length / 1024).toFixed(1)} KB raw`)
